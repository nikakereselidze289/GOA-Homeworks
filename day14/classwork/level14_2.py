num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

for i in range(num1, num2 + 1):
    print(f"კვადრატი: {i ** 2}")
    print(f"სტ. ცვლადი + პირველი რიცხვი: {i + num1}")
    print(f"სტ. ცვლადი + მეორე რიცხვი: {i + num2}")
    print(f"სტ. ცვლადი * პირველი რიცხვი: {i * num1}")
    print(f"სტ. ცვლადი * მეორე რიცხვი: {i * num2}")
